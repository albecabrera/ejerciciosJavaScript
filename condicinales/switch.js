let codigo = 1515
let codigoSeguridad

switch (codigo) {
    case codigoSeguridad = 1515:
        console.log("Atención, la puerta se va a abrir!");
    break
    case codigoSeguridad = 2020:
        console.log("Código incorrecto, acceso denegado.");
    break
    case codigoSeguridad = 3030:
        console.log("Código de emergencia activado.");
    break
    case codigoSeguridad = 4040:
        console.log("Código no reconocido, intente nuevamente.");
    break
    default:
        console.log("Error: Código no válido.");
    break
}

